// Función clásica para multiplicar dos números
function multiplica_clasica(num1, num2) {
    return num1 * num2;
}
// Funcion flecha para multiplicar dos números
const multiplica_flecha = (num1, num2) => num1 * num2;
// Función clasica para elevar al cuadrado
function elevaAlCuadrado_clasica(num) {
    return num * num;
}
// Función flecha para elevar al cuadrado
const elevaAlCuadrado_flecha = num => num * num;
